using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class Corridor
{
    public int x;
    public int y;
    public int width;
    public int height;

    public Corridor(int xPos, int yPos, int w, int h)
    {
        x = xPos;
        y = yPos;
        width = w;
        height = h;

    }
}
